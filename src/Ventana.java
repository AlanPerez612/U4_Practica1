    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int renglon = jTable1.getSelectedRow();
        if(renglon==-1){
            showMessageDialog(this,"Error, no has seleccionado nada a borrar.");
            return ;
        }
        String idBorrar=tabla.getValueAt(renglon, 0).toString();
        boolean resultado = mySQL.eliminar(idBorrar);
        if(resultado) {
            actualizarTable();
            JOptionPane.showMessageDialog(this,"¡Se eliminó correctaente!");
        }
        else{
            JOptionPane.showMessageDialog(this,"No se pudo eliminar la tabla.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        Producto productoInsertar = new Producto(txtDesc.getText(),Float.parseFloat(txtPrecio.getText()),Integer.parseInt(txtExist.getText()));

        boolean resultadoInsert = mySQL.insertar(productoInsertar);
        if(resultadoInsert){
            JOptionPane.showMessageDialog(this, "¡Se insertó correctamente!");
            txtDesc.setText("");txtPrecio.setText("");txtExist.setText("");
            actualizarTable();
        }else{
            JOptionPane.showMessageDialog(this, "Error al insertar los datos.");
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

