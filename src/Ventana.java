    public void actualizarTable(){
        ArrayList<String[]> datosSQL = mySQL.consultarDatos();
        if(datosSQL.size()==0)return;
        
        int total = tabla.getRowCount();
        for (int i = 0; i < total; i++) {
            tabla.removeRow(0);
        }
        
        for (int i = 0; i < datosSQL.size(); i++) {
            String[] renglon = datosSQL.get(i);
            tabla.addRow(renglon);
        }
    }

